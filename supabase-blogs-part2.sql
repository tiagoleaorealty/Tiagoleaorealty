## 1. Titled or concession?

First question, always. Most inland Guanacaste lots are fee-simple titled land. Within the maritime zone - the first 200 meters from the high-tide line - land is generally state-owned and held by renewable municipal **concession**, with different rules for foreigners and corporations. Full explainer: [Titled vs. Concession Beachfront](https://soldbytiago.com/blog/titled-vs-concession-beachfront-costa-rica/).

## 2. Land-use certificate (uso de suelo)

Request the municipality''s uso de suelo for the lot early. It states permitted use and building parameters - density, coverage, height, setbacks - under the canton''s zoning plan (plan regulador) where one exists. Not every canton has a complete, current plan, which itself is information: where zoning is thin, your engineer and attorney carry more of the verification burden.

## 3. Water - the make-or-break item

No valid **water availability letter**, no building permit. Confirm who serves the area (AyA, an ASADA, municipal system or a community''s own concession), whether a current letter exists for this exact lot, and what its text actually commits. The differences between a meter, a promise and a permit-grade letter are covered in [What Is a Water Letter?](https://soldbytiago.com/blog/costa-rica-property-water-letter-guide/) - read it before touring lots, not after.

## 4. Electricity

Guanacaste is served by ICE and, across much of this coast, by the rural cooperative [Coopeguanacaste](https://www.coopeguanacaste.com/en/), which handles new-service availability inquiries through its official channels. Confirm where the nearest connection point is and what extending service to your build site involves - remote-feeling lots can carry very real line-extension costs.

## 5. Legal road access

The lot must touch a public road or hold a properly **registered easement** (servidumbre) that appears in the Registro Nacional. A driveway you can drive today is not the same as access you legally own tomorrow. Landlocked-with-a-handshake is a classic Guanacaste trap.

## 6. Topography

Slope drives buildability and cost: cut-and-fill, retaining, foundations, driveway grade. A steep view lot can cost dramatically more to build on than a flatter one nearby. Walk the land with your architect or engineer before you fall in love with the view.

## 7. Soil and drainage studies

For septic systems - the norm outside a few served areas - your engineer will need soil/percolation information, and sanitary approval is part of the permit chain. Wet-season behavior matters too: a lot that is perfect in February can carry runoff in October. Budget for a soils study on any serious candidate.

## 8. Setbacks and protection zones

Costa Rica''s Forestry Law ([Ley 7575](https://pgrweb.go.cr/scij/Busqueda/Normativa/normas/nrm_texto_completo.aspx?nValor1=1&nValor2=41661)) establishes protected zones you cannot build in: commonly cited as 10 meters alongside rivers in flat urban terrain, 15 meters in rural terrain, up to 50 meters on steep slopes, and a 100-meter radius around permanent springs (200 meters where a spring supplies a community). Road setbacks (retiro frontal) come from the zoning rules. Which zones touch your lot is a technical determination for your surveyor and engineer - streams that look like ditches in dry season count.

## 9. Environmental review (SETENA)

Construction projects pass through environmental screening with [SETENA](https://www.setena.go.cr/), the national environmental secretariat; the depth of review depends on the project''s category, and low-impact single homes follow lighter paths than developments. Your architect or engineer determines the category and files accordingly - what you need at offer time is awareness that environmentally sensitive features (forest cover, waterways, coastal proximity) can change what is approvable.

## 10. The permit chain

For a home build, expect this sequence: uso de suelo → design by a licensed professional → plan approval through the engineers'' and architects'' college ([CFIA](https://www.cfia.or.cr/)) via its digital APC system → institutional approvals (water availability, electricity, sanitary) → the municipal construction permit, plus the mandatory INS workers'' insurance policy before breaking ground. Timelines vary with complexity; your professional runs this, but your purchase timing should anticipate it.

## 11. Survey verification

Have a licensed surveyor confirm the registered plano catastrado matches the ground - boundaries, area, encroachments. Title-vs-survey discrepancies are common and must be fixed before closing: [how planos and titles work](https://soldbytiago.com/blog/costa-rica-property-title-plano-catastrado-guide/).

## 12. HOA and development rules

Inside a gated community, the development''s design guidelines, build timelines, and fee obligations bind your lot. Get the current documents - not a summary - and read the construction sections before offering. My community guides (for example [Las Ventanas](https://soldbytiago.com/development/las-ventanas-playa-grande/) and [Mar Vista](https://soldbytiago.com/development/mar-vista/)) flag what each development publishes and what you must request.

