## The approach

Lakeside teaches through an integrated curriculum built on the **Multiple Intelligences** model — the idea, in their words, that intelligence "differentiates into specific modalities, rather than seeing it as dominated by a single general ability."

In practice that means a kid who is not a strong test-taker but thinks in music, movement or images is not written off as a weak student. Whether that is a good fit depends entirely on your kid.

Their stated aim is to "prepare our students to be successful, confident, bi-lingual individuals who are prepared for the emerging global community." They also hold Costa Rica''s Ecological Blue Flag.

## Being honest about the gaps

Lakeside publishes less than the bigger campuses do — no student numbers, no class sizes, no facilities list, no tuition. That is not a knock; it is just a smaller school with a smaller marketing operation. But it does mean the visit is doing more work here than the website.

